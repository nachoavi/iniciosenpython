
def sumar(a,b):
    result = a + b
    return result
def restar(a,b):
    result = a -b
    return result
def multiplicar(a,b):
    result = a*b
    return result
def dividir(a,b):
    if b != 0:
        result = a/b
    else:
        print("No se pueden realizar diviciones por cero")
    return result
