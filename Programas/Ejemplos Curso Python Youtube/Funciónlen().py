#Clase de función len()
#La función len() nos permite obtener la longitud de una cadena de caracteres, o bien, el número de elementos que componen a un objeto

#Sintaxis: len("Objeto o cadena de caracteres")

#EJEMPLOS: 

#Opción 1
print("Hola tiene", len("Hola"), "caracteres")


#Opción 2

longitud = len("Inacap")
print("Inacap tiene ",longitud, "caracteres")
